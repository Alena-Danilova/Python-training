# Cycles

name = "hello"
for a in name:
    print(a)

# List
shows = ["1", "2", "3"]
for b in shows:
    print(b)

# The motorcade
r = ("a", True, 3)
for w in r:
    print(w)

# Dictionary
u = {"key": 1,
     "cat": "Barsic",
     "value": True,}
for c in u:
    print(c)

